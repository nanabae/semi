CREATE TABLE MEMBER (
	MEM_NUM	NUMBER		NOT NULL,
	MEM_ID	VARCHAR2(100)		NULL,
	MEM_PWD	VARCHAR2(100)		NULL,
	MEM_NICK	VARCHAR2(100)		NULL,
	MEM_BIRTH	CHAR(6)		NULL,
	MEM_PHON	CHAR(11)		NULL,
	MEM_POINT	NUMBER		NULL,
	MEM_STATUS	CHAR(1)		DEFAULT 'O' CHECK (MEM_STATUS IN ('O','X'))
);
CREATE SEQUENCE SEQ_MEMBER_NO NOCACHE NOCYCLE;


CREATE TABLE ADMIN (
	ADMIN_NUM	NUMBER		NOT NULL,
	MEM_NUM	NUMBER		NOT NULL,
	ADMIN_PWD	VARCHAR2(100)		NULL,
	ADMIN_LEVEL	NUMBER		NULL
);
CREATE SEQUENCE SEQ_ADMIN_NO NOCACHE NOCYCLE;


CREATE TABLE MEMO (
	MEMO_NUM	NUMBER		NOT NULL,
	MEMO_WRITER	NUMBER		NOT NULL,
    MEMO_TARGET	VARCHAR(100)		NOT NULL,
	MEMO_CONTENT	VARCHAR(100)		NULL,
    MEMO_ENROLL_DATE	TIMESTAMP		 DEFAULT SYSDATE
);
CREATE SEQUENCE SEQ_MEMO_NO NOCACHE NOCYCLE;


CREATE TABLE MESSAGE (
	MSG_NUM	NUMBER		NOT NULL,
	MSG__SENDER	VARCHAR2(100)		NOT NULL,
	MSG_RECEIVER	VARCHAR2(100)		NOT NULL,
	MSG__CONTENT	VARCHAR2(1000)		NULL,
	MSG__ENROLL_DATE	TIMESTAMP		 DEFAULT SYSDATE,
	MSG__TITLE	VARCHAR2(100)		NULL,
	MSG_STATUS	CHAR(1)		DEFAULT 'O' CHECK (MSG_STATUS IN ('O','X')),
	MSG_READ_DATETIME	TIMESTAMP		 
);
CREATE SEQUENCE SEQ_MESSAGE_NO NOCACHE NOCYCLE;


CREATE TABLE CATEGORY (
	CAT_NUM	NUMBER		NOT NULL,
	CAT_NAME	VARCHAR(100)		NULL
);
CREATE SEQUENCE SEQ_CATEGORY_NO NOCACHE NOCYCLE;


CREATE TABLE HEADER (
	HEADER_NUM	NUMBER		NOT NULL,
	HEADER_NAME	VARCHAR2(100)		NULL
