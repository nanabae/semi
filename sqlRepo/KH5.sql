	MEM_HEAD_NUM	NUMBER		NOT NULL,
	MEM_NUM	NUMBER		NOT NULL,
	HEADER_NUM	NUMBER		NOT NULL
);
CREATE SEQUENCE SEQ_MEM_HEADER_NO NOCACHE NOCYCLE;


CREATE TABLE DRAMA_BOARD (
	DRAMA_BRD_NUM	NUMBER		NOT NULL,
	DRAMA_WRITER	NUMBER		NOT NULL,
	CAT_NUM	NUMBER		NOT NULL,
    HEADER_NUM NUMBER ,
	TITLE	VARCHAR2(100)		NULL,
	CONTENT	VARCHAR2(4000)		NULL,
	STATUS	CHAR(1)		DEFAULT 'O' CHECK (STATUS IN ('O','X')),
	ENROLL_DATE	TIMESTAMP		 DEFAULT SYSDATE,
	MODIFY_DATE	TIMESTAMP		NULL,
	HIT	NUMBER		DEFAULT 0,
	ANONYMITY	CHAR(1)		DEFAULT 'X' CHECK (ANONYMITY IN ('O','X'))
);
CREATE SEQUENCE SEQ_DRAMA_BOARD_NO NOCACHE NOCYCLE;


CREATE TABLE NOTICE_BOARD (
	NOTICE_NUM	NUMBER		NOT NULL,
	ADMIN_NUM	NUMBER		NOT NULL,
	TITLE	VARCHAR2(100)		NULL,
	CONTENT	VARCHAR2(4000)		NULL,
	STATUS	CHAR(1)		DEFAULT 'O' CHECK (STATUS IN ('O','X')),
	ENROLL_DATE	TIMESTAMP		DEFAULT SYSDATE,
	MODIFY_DATE	TIMESTAMP		NULL,
	HIT	NUMBER		DEFAULT 0
);
CREATE SEQUENCE SEQ_NOTICE_BOARD_NO NOCACHE NOCYCLE;


CREATE TABLE DRAMA_BOARD_REPLY (
	RE_NO	NUMBER		NOT NULL,
	RE_WRITER	NUMBER		NOT NULL,
	DRAMA_BRD_NUM	NUMBER		NOT NULL,
	RE_REF	NUMBER		 NOT NULL,
	RE_CONTENT	VARCHAR2(1000)	NOT	NULL,
	RE_ANONYMITY	CHAR(1)		DEFAULT 'X' CHECK (RE_ANONYMITY IN ('O','X')),
	RE_ENROLL_DATE	TIMESTAMP		DEFAULT SYSDATE,
	RE_STATUS	CHAR(1)		DEFAULT 'O' CHECK (RE_STATUS IN ('O','X'))
);
CREATE SEQUENCE SEQ_DRAMA_BOARD_REPLY_NO NOCACHE NOCYCLE;


CREATE TABLE NOTICE_BOARD_REPLY (
	NOTICE_RE_NO	NUMBER		NOT NULL,
