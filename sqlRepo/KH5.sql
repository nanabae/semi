	RE_REF	NUMBER		NULL,
	RE_ANONYMITY	CHAR(1)		DEFAULT 'X' CHECK (RE_ANONYMITY IN ('O','X'))
);
CREATE SEQUENCE SEQ_NOTICE_BOARD_REPLY_NO NOCACHE NOCYCLE;


